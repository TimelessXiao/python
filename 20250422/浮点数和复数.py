# 复数由实部和虚部组成, 其中虚部用j表示
a = 1 + 2j
b = 2 + 3j
# 两个复数相加，实部与实部相加，虚部与虚部相加
c = a + b
print(c)
# 使用函数complex,定义复数
d = complex(1, 2)
print(d)
# 获取复数的实部和虚部
# 使用函数real,获取实部
print(d.real)
# 使用函数imag,获取虚部
print(d.imag)

